package com.company;

public class SMH_08_Ch2_Bitwise_Operator {
    public static void main(String[] args) {
        // Bitwise AND (&) Operator
        int a = 2;
        int b =3;
        int c = a & b;
        System.out.println(c);

        // Bitwise OR (|) Operator
        int d = 2;
        int e =3;
        int f = d | e;
        System.out.println(f);
    }
}
