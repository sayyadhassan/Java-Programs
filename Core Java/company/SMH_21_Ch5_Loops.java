package com.company;

public class SMH_21_Ch5_Loops {
    public static void main(String[] args) {
        int i=100;
        while(i>=100 && i<=200)
        {
            System.out.print(i+" ");
            i++;
            if(i==131)
            {
                System.out.println();
            }
            else if(i==161)
            {
                System.out.println();
            }
        }
    }
}
